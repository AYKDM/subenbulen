    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(null, "Enter the ID of the student you want to search");
        if(input != null){
            list = Database.findbyname(input);
            model.setRowCount(0);
            for(student s : list){
                model.addRow(new Object[]{s.getId(), s.getName(), s.getAge(), s.getGender(), s.getAddress(), s.getEmail(), s.getPhone()});
            }
        }
    }//GEN-LAST:event_jButton9ActionPerformed

