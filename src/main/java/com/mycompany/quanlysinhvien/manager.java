    private void addTable(){
        List<student> list = Database.selectAll();
        model.setRowCount(0);
        list.forEach(s -> model.addRow(new Object[]{s.getId(), s.getName(), s.getAge(), s.getGender(), s.getAddress(), s.getEmail(), s.getPhone()}));
    }
