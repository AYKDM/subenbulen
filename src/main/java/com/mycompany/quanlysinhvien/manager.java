    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String n = name.getText();
        int a = Integer.parseInt(age.getText());
        String g = jComboBox1.getSelectedItem().toString();
        String ad = add.getText();
        String em = email.getText();
        int p = Integer.parseInt(phone.getText());
        student s = new student(n, a, g, ad, em, p);
        Database.insert(s);
        addTable();
        name.setText("");
        age.setText("");
        jComboBox1.setSelectedIndex(0);
        add.setText("");
        email.setText("");
        phone.setText("");

    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        student s = new student();
        s.setId(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));
        s.setName(name.getText());
        s.setAge(Integer.parseInt(age.getText()));
        s.setGender(jComboBox1.getSelectedItem().toString());
        s.setAddress(add.getText());
        s.setEmail(email.getText());
        s.setPhone(Integer.parseInt(phone.getText()));
        if(JOptionPane.showConfirmDialog(this, "Do you want to update this record?") == 0){
            Database.update(s);
            JOptionPane.showMessageDialog(this, "Record updated successfully");
            addTable();
        }
        else
            JOptionPane.showMessageDialog(this, "Record not updated");

    }//GEN-LAST:event_jButton8ActionPerformed

